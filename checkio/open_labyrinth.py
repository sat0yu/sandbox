def explorer(maze_map, pos, route):
    if pos == (10,10):
        return route
    elif maze_map[pos[0] + 1][pos[1]] == 0:
        maze_map[pos[0]][pos[1]] = "S"
        return explorer(maze_map, (pos[0] + 1, pos[1]), route + "S")
    elif maze_map[pos[0]][pos[1] - 1] == 0:
        maze_map[pos[0]][pos[1]] = "W"
        return explorer(maze_map, (pos[0], pos[1] - 1), route + "W")
    elif maze_map[pos[0] - 1][pos[1]] == 0:
        maze_map[pos[0]][pos[1]] = "N"
        return explorer(maze_map, (pos[0] - 1, pos[1]), route + "N")
    elif maze_map[pos[0]][pos[1] + 1] == 0:
        maze_map[pos[0]][pos[1]] = "E"
        return explorer(maze_map, (pos[0], pos[1] + 1), route + "E")
    else:
        maze_map[pos[0]][pos[1]] = -1
        back = {"N": (1, 0), "S": (-1, 0), "E": (0, -1), "W": (0, 1)}.get(route[-1], None)
        pos = pos[0] + back[0], pos[1] + back[1]
        return explorer(maze_map, pos, route[:-1])

